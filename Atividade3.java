public class Atividade3 {
 
    public static void main(String[] args) {
        // Exemplo de teste da função
        double resultado = somarTres(10.5, 20.0, 5.5);
        System.out.println("A soma dos três argumentos é: " + resultado);
    }

    // Função que recebe três argumentos e retorna a soma
    public static double somarTres(double a, double b, double c) {
        return a + b + c;
    }
}  

